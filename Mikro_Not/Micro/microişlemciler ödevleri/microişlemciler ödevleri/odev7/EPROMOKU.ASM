MOVF YUZLER,W
MOVWF PORTD
CLRF PORTD

MOVLW H'02'
MOVWF PORTA
MOVF ONLAR,W
MOVWF PORTD
CLRF PORTD

MOVLW H'01'
MOVWF PORTA
MOVF BIRLER,W
MOVWF PORTD
CLRF PORTD

