BSF STATUS,RP0
BCF EECON1,7
BSF EECON1,RD
BCF STATUS,RP0 ;BANK3
MOVF EEDATA,W
BCF STATUS,RP1 ;BANK0
MOVWF BINLER

BSF STATUS,RP1
MOVLW H'04'
MOVWF EEADR
BSF STATUS,RP0
BCF EECON1,7
BSF EECON1,RD
BCF STATUS,RP0 ;BANK3
MOVF EEDATA,W
BCF STATUS,RP1 ;BANK0
MOVWF YUZLER

BSF STATUS,RP1
MOVLW H'03'
MOVWF EEADR
BSF STATUS,RP0
BCF EECON1,7
BSF EECON1,RD
BCF STATUS,RP0 ;BANK3
MOVF EEDATA,W
BCF STATUS,RP1 ;BANK0
MOVWF ONLAR

BSF STATUS,RP1
MOVLW H'02'
MOVWF EEADR
BSF STATUS,RP0
BCF EECON1,7
BSF EECON1,RD
BCF STATUS,RP0 ;BANK3
MOVF EEDATA,W
BCF STATUS,RP1 ;BANK0
MOVWF BIRLER

RETURN


BASLANGIC
BSF STATUS,RP0 ;BANK1
MOVLW B'10000000'
MOVWF OPTION_REG
CLRF TRISA ;A PORTU ÇIKIÞA AYARLANDI
CLRF TRISD ;D PORTU ÇIKIÞA AYARLANDI
